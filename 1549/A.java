import java.util.*;
import java.lang.*;
import java.io.*;
public class Solution
{
      public static void main(String args[]) throws java.lang.Exception
      {
            Scanner sc=new Scanner(System.in);
            int t=sc.nextInt();
            while(t>0)
            {
                  long p=sc.nextLong();
                  System.out.println(2+" "+((p/2)*2));
                  t-=1;
                  
            }
            
            
      }
      
}