import java.util.*;
import java.lang.*;
import java.io.*;
public class Solution
{
      public static void main(String args[]) throws java.lang.Exception
      {
            Scanner sc=new Scanner(System.in);
            int t=sc.nextInt();
            while(t>0)
            {
                  String s=sc.next();
                  int l=s.length();
                  System.out.println(l);
                  t-=1;
            }
      }
      
}